class Logical_operator 
{
	public static void main(String[] args) 
	{
		System.out.println(4<5&&10>12);
		System.out.println(10<12&&5==5);
		System.out.println(11<10&&12<13);
		System.out.println(5!=5&&12<10);
		System.out.println(10>8||20!=20);
		System.out.println(5==5||6>8);
		System.out.println(4<2||5==5);
		System.out.println(5<=5||6<7);
		System.out.println(!(5!=5));
		System.out.println(!(5<9));
	}
}
